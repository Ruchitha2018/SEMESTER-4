ASSUME CS:CODE, DS:DATA
DATA SEGMENT
LIST DW 20H,25H,0ABH,022H,23H
CNT EQU 05H
EVN DW ?
OD DW ?
DATA ENDS

CODE SEGMENT
START: MOV AX,DATA
       MOV DS,AX
       XOR AX,AX
       MOV CX,CNT
       MOV SI, OFFSET LIST
BACK:  MOV AL, [SI]
       ROR AL, 01H
       JC NEXT
       INC EVN
       JMP AHEAD
NEXT:  INC OD
AHEAD: ADD SI, 01H
       LOOP BACK
       MOV AH,4CH
       INT 21H
CODE ENDS
END START
